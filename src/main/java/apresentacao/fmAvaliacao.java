    private void btCalcularIMCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCalcularIMCActionPerformed
       try {
        // 1. Pegar o texto e trocar v√≠rgula por ponto (pra n√£o dar erro se digitar 1,75)
        String strPeso = txtPeso.getText().replace(",", ".");
        String strAltura = txtAltura.getText().replace(",", ".");
        
        // 2. Converter para n√∫meros (double)
        double peso = Double.parseDouble(strPeso);
        double altura = Double.parseDouble(strAltura);
        
        // 3. A F√≥rmula M√°gica: Peso dividido por (Altura vezes Altura)
        double imc = peso / (altura * altura);
        
        // 4. Formatar para mostrar s√≥ 2 casas decimais (ex: 24.50)
        String resultadoFormatado = String.format("%.2f", imc);
        
        // 5. Mostrar no Label
        // Verifica o status s√≥ pra ficar bonito
        String status = "";
        if (imc < 18.5) status = " (Abaixo do Peso)";
        else if (imc < 25) status = " (Peso Normal)";
        else if (imc < 30) status = " (Sobrepeso)";
        else status = " (Obesidade)";
        
        lblResultadoIMC.setText("IMC: " + resultadoFormatado + status);
        
    } catch (NumberFormatException e) {
        // Se a pessoa digitar letras ou deixar vazio
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, digite n√∫meros v√°lidos no Peso e Altura!");
    } 
    }//GEN-LAST:event_btCalcularIMCActionPerformed

    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        try {
            // 1. VALIDA√á√ÉO B√ÅSICA
            if (cbAluno.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Selecione um aluno!", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

            if (cbFuncionario.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Selecione um instrutor!", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // 2. PEGAR VALORES
            String strPeso = txtPeso.getText().trim().replace(",", ".");
            String strAltura = txtAltura.getText().trim().replace(",", ".");
            String strGordura = txtGordura.getText().trim().replace(",", ".");
            String observacoes = txtObservacoes.getText().trim();

            // 3. CONVERTER E VALIDAR LIMITES
            double peso, altura, gordura = 0;

            try {
                // Peso: 0.1 a 300 kg
                peso = Double.parseDouble(strPeso);
                if (peso < 0.1 || peso > 300) {
                    JOptionPane.showMessageDialog(this, 
                        "Peso inv√°lido! (0.1 a 300 kg)", 
                        "Valor fora do limite", 
                        JOptionPane.WARNING_MESSAGE);
                    txtPeso.requestFocus();
                    return;
                }
                
                // Altura: 0.5 a 3.0 metros
                altura = Double.parseDouble(strAltura);
                if (altura < 0.5 || altura > 3.0) {
                    JOptionPane.showMessageDialog(this, 
                        "Altura inv√°lida! (0.5 a 3.0 metros)", 
                        "Valor fora do limite", 
                        JOptionPane.WARNING_MESSAGE);
                    txtAltura.requestFocus();
                    return;
                }
                
                // Gordura: 0 a 100% (opcional)
                if (!strGordura.isEmpty()) {
                    gordura = Double.parseDouble(strGordura);
                    if (gordura < 0 || gordura > 100) {
                        JOptionPane.showMessageDialog(this, 
                            "Gordura corporal inv√°lida! (0 a 100%)", 
                            "Valor fora do limite", 
                            JOptionPane.WARNING_MESSAGE);
                        txtGordura.requestFocus();
                        return;
                    }
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, 
                    "Digite apenas n√∫meros nos campos!\n" +
                    "Use ponto ou v√≠rgula para decimais.\n" +
                    "Ex: 70.5 ou 70,5", 
                    "Formato inv√°lido", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }

            // 4. EXTRAIR IDs
            int idAluno = extrairIdDoCombo(cbAluno);
            int idInstrutor = extrairIdDoCombo(cbFuncionario);

            if (idAluno <= 0 || idInstrutor <= 0) {
                JOptionPane.showMessageDialog(this, 
                    "IDs inv√°lidos!\nAluno: " + idAluno + "\nInstrutor: " + idInstrutor, 
                    "Erro", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }

            // 5. CONFIRMA√á√ÉO
            int confirm = JOptionPane.showConfirmDialog(this, 
                "Salvar avalia√ß√£o f√≠sica?\n\n" +
                "Aluno ID: " + idAluno + "\n" +
                "Instrutor ID: " + idInstrutor + "\n" +
                "Peso: " + peso + " kg\n" +
                "Altura: " + altura + " m", 
                "Confirmar", 
                JOptionPane.YES_NO_OPTION);

            if (confirm != JOptionPane.YES_OPTION) {
                return;
            }

            // 6. CRIAR OBJETO
            negocio.AvaliacaoFisica avaliacao = new negocio.AvaliacaoFisica();
            avaliacao.setPeso(peso);
            avaliacao.setAltura(altura);
            avaliacao.setGorduraCorporal(gordura);
            avaliacao.setObservacoes(observacoes);
            avaliacao.setDataAvaliacao(new java.util.Date());
            avaliacao.setIdAluno(idAluno);
            avaliacao.setIdInstrutor(idInstrutor);

            // 7. SALVAR NO BANCO
            IAvaliacaoFisicaDAO dao = new AvaliacaoFisicaDAO();
            dao.adiciona(avaliacao);

            // 8. MENSAGEM DE SUCESSO
            JOptionPane.showMessageDialog(this, 
                "‚úÖ Avalia√ß√£o salva com sucesso!\n\n" +
                "üìã Resumo:\n" +
                "‚Ä¢ Aluno ID: " + idAluno + "\n" +
                "‚Ä¢ Instrutor ID: " + idInstrutor + "\n" +
                "‚Ä¢ Peso: " + String.format("%.1f", peso) + " kg\n" +
                "‚Ä¢ Altura: " + String.format("%.2f", altura) + " m\n" +
                "‚Ä¢ Gordura: " + (gordura > 0 ? String.format("%.1f", gordura) + "%" : "N√£o informado") + "\n" +
                "‚Ä¢ Data: " + new SimpleDateFormat("dd/MM/yyyy").format(new java.util.Date()), 
                "Sucesso", 
                JOptionPane.INFORMATION_MESSAGE);

            // 9. LIMPAR CAMPOS
            limparCampos();

        } catch (Exception e) {
            // TRATAMENTO ESPEC√çFICO PARA "OUT OF RANGE"
            String mensagemErro = "‚ùå Erro ao salvar!\n";
            
            if (e.getMessage() != null) {
                if (e.getMessage().contains("out of range") || 
                    e.getMessage().contains("Data truncation") ||
                    e.getMessage().contains("value too large")) {
                    mensagemErro += "\nüëâ VALOR FORA DOS LIMITES PERMITIDOS!\n\n" +
                                  "Limites do banco:\n" +
                                  "‚Ä¢ Peso: at√© 999.99 kg\n" +
                                  "‚Ä¢ Altura: at√© 9.99 metros\n" +
                                  "‚Ä¢ Gordura: at√© 100%\n\n" +
                                  "Ajuste os valores e tente novamente.";
                } else if (e.getMessage().contains("foreign key constraint")) {
                    mensagemErro += "\nüëâ ALUNO OU INSTRUTOR N√ÉO EXISTE!\n\n" +
                                  "Verifique se os IDs selecionados existem no banco.";
                } else {
                    mensagemErro += "\nDetalhe: " + e.getMessage();
                }
            }
            
            JOptionPane.showMessageDialog(this, 
                mensagemErro, 
                "Erro", 
                JOptionPane.ERROR_MESSAGE);
            
            // Log para debug
            System.err.println("ERRO AO SALVAR AVALIA√á√ÉO:");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

